public class SwitchCase {
    public static void main(String[] args){
        int imiona =5;
         switch(imiona){
             case 1:
                 System.out.println("MAREK");
                 break;
             case 2:
                 System.out.println("JAN");
                 break;
             case 3:
                 System.out.println("RAFAL");
                 break;
             case 4:
                 System.out.println("ADRIAN");
                 break;
             default:
                 System.out.println("Nie ma takiego imienia");
                 break;
         }
    }
}
